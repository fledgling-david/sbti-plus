def calculate_result(selected_options: List[int]) -> TestResponse:
    """
    计算测试结果
    """
    scores: Dict[str, int] = {
        '卷王': 0,
        '脆皮': 0,
        '缝合怪': 0,
        '乐子人': 0,
        '纯爱战神': 0,
        '网抑云患者': 0,
        '社交牛杂症': 0,
        '摸鱼大师': 0
    }

    for i, option_index in enumerate(selected_options):
        if 0 <= option_index < len(questions[i].options):
            option = questions[i].options[option_index]
            for personality, weight in option.weights.items():
                scores[personality] += weight

    # 找出得分最高的人格类型
    highest_score = max(scores.values())
    highest_personalities = [name for name, score in scores.items() if score == highest_score]
    
    # 如果有多个最高得分，随机选择一个
    import random
    selected_personality_name = random.choice(highest_personalities)
    
    # 找到对应的人格类型
    selected_personality = next(p for p in personalities if p.name == selected_personality_name)

    return TestResponse(
        personality=selected_personality,
        scores=scores
    )
